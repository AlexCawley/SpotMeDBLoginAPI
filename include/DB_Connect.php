<?php

/*------------------------------------------------------------------------------------------------*
 *  Database Connection                                                                           *
 *------------------------------------------------------------------------------------------------*/
class DB_CONNECT {
 
 	/*--------------------------------------------------------------------------------------------*
 	 *  Member Variables                                                                          *
 	 *--------------------------------------------------------------------------------------------*/
	private $conn;

	/*--------------------------------------------------------------------------------------------*
	 *  Constructor                                                                               *
     *--------------------------------------------------------------------------------------------*/
    function __construct() 
    {
        $this->connect();
    }

    function __destruct() 
    {
    }
 
 	/*--------------------------------------------------------------------------------------------*
     *                                                                                            *
     *  Connect                                                                                   *
     *                                                                                            *
     *--------------------------------------------------------------------------------------------*
     *  Connect to the database given in the config file                                          *
     *--------------------------------------------------------------------------------------------*/
    function connect() 
    {
    	// Get the config credentials
        require_once 'config.php';
 
 		// Connect to the database or throw an error
        $this->conn = mysqli_connect(DB_HOST, DB_USER, DB_PASSWORD, DB_DATABASE) or die(mysql_error());
 
 		// Return the connection
        return $this->conn;
    }
 
}
 
?>